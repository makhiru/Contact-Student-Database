package com.example.contactstudent.Models;

public class Student {
    int id;
    String name, fname, sname, rollno;

    public Student(int id, String name, String fname, String sname, String rollno) {
        this.id = id;
        this.name = name;
        this.fname = fname;
        this.sname = sname;
        this.rollno = rollno;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFname() {
        return fname;
    }

    public void setFname(String fname) {
        this.fname = fname;
    }

    public String getSname() {
        return sname;
    }

    public void setSname(String sname) {
        this.sname = sname;
    }

    public String getRollno() {
        return rollno;
    }

    public void setRollno(String rollno) {
        this.rollno = rollno;
    }
}
